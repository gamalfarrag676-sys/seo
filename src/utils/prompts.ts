/**
 * SEO rules shared across all prompts — ensures consistent quality scoring
 */
function getSEORules(keyword: string): string {
  return `
  🚨🚨🚨 **قواعد SEO إلزامية (MUST FOLLOW - سيتم قياسها تلقائياً):** 🚨🚨🚨
  
  1. **العنوان (H1) - إلزامي:**
     - ✅ يجب أن يبدأ حرفياً بـ "${keyword}" في أول 10 أحرف
     - ✅ الطول: 30-70 حرف فقط
     - ❌ لا تضع أي كلمات قبل الكلمة المفتاحية
  
  2. **المقدمة (أول 100 حرف) - إلزامي:**
     - ✅ أول جملة يجب أن تبدأ بـ "${keyword}" أو تحتويها في أول 50 حرف
     - ✅ لا تبدأ بـ "هل تبحث" أو "إذا كنت" - ابدأ مباشرة بالكلمة المفتاحية
     - مثال صحيح: "${keyword} هو الخيار الأمثل لمن يبحث عن..."
  
  3. **كثافة الكلمة المفتاحية - إلزامي:**
     - ✅ كرر "${keyword}" بنسبة 1.5-2.5% من إجمالي الكلمات (تقريباً 5-8 مرات في 300 كلمة)
     - ✅ استخدم أيضاً صيغ مشتقة: "ال${keyword}"، "${keyword} الأصلي"
     - ✅ وزّع التكرارات بطبيعية في كل قسم
  
  4. **وصف الميتا (META_DESCRIPTION) - إلزامي:**
     - ✅ الطول: 120-155 حرف بالضبط (لا أقل ولا أكثر)
     - ✅ يجب أن يحتوي على "${keyword}" مرة واحدة على الأقل
     - ✅ ابدأ بالكلمة المفتاحية أو ضعها في أول 50 حرف
  
  5. **المحتوى الرئيسي:**
     - ✅ 3+ فقرات منفصلة بسطر فارغ
     - ✅ أكثر من 300 كلمة إجمالاً
     - ✅ استخدم كلمات قوية: مميز، حصري، أفضل، جديد، فريد، احترافي، مضمون`;
}

/**
 * Product description context block (shared between expert & simple prompts)
 */
function getProductContext(productName: string, keyword: string): string {
  return `${productName ? `
  🔴🔴🔴 **تعليمات إلزامية - وصف المنتج المقدم من المستخدم:** 🔴🔴🔴
  المستخدم قدم لك وصفاً للمنتج، يجب أن تلتزم به تماماً ولا تتجاهله:
  "${productName}"
  
  استخدم هذا الوصف كمرجع أساسي لكتابة المحتوى. لا تخترع معلومات تتعارض مع هذا الوصف.
  ركز على المميزات والتفاصيل المذكورة فيه.` : '⚠️ لم يُقدم وصف للمنتج - اعتمد على تحليل الصورة فقط'}
  ${keyword ? `الكلمة المفتاحية المستهدفة: ${keyword}` : ''}`;
}

